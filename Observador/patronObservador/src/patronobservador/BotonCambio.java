    @Override
    public void update( Observable observable, Object obj ) {
        String temperatura = obj.toString();
        temperaturaActual.setText(temperatura);
        seleccionarColor();
    }
    
    private void seleccionarColor() {
        float temperatura = Float.parseFloat( temperaturaActual.getText() );
        
        if( temperatura < 0 )
            temperaturaActual.setForeground( Color.blue );
        else if( temperatura > 50 )
            temperaturaActual.setForeground( Color.red );
        else
            temperaturaActual.setForeground( Color.green );
        
    }
    
